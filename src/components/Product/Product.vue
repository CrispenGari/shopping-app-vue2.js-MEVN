<template>
  <div class="product">
    <img :src="product.img" alt="product" />
    <div class="product__info">
      <p>{{ product.name }}</p>
      <p>$ {{ product.price }}</p>
      <button class="product__add" @click="addToBasket(product)">
        Add to Cart
      </button>
    </div>
  </div>
</template>

<script>
import axios from "../../axios";
export default {
  name: "Product",
  props: {
    product: Object,
  },
  data() {
    return {};
  },
  methods: {
    addToBasket(product) {
      (async () => {
        await axios.post("/basket/add", product);
      })();
    },
  },
};
</script>

